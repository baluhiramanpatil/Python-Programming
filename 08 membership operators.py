# membership operators (in, not in)

'''
in (return true if a sequence with the specified value is present in the object)
not in(return true if a sequence with the specified value is not present in the object)

'''
x = 'hello world'

print('h' in x)
print('p' in x)
print('p' not in x)


y = [10, 20, 30, 40, 50]

print(10 in y)
print(10 not in y)
print(100 in y)
print(100 not in y)
print('h' in y)
print('h' not in y)


