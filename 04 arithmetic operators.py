# arithmetic operator (+, -, *, /,  %, **, //)

'''
+ addition
- subtraction
* multiplication
/ division
% modulus / remainder 
** exponents
// floor divison

'''
a = 10
b = 20

print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)
print(a % 3)
print(a ** 2)
print(a // b)


print(5 ** 2) # 5 * 5
print(5 ** 3) # 5 * 5 * 5


print(10 / 3)
print(10 // 3) # floor divison  